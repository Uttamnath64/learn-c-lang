#include<stdio.h>

int main(){
    int num1 = 10, num2 = 2;

    printf("Addition: %d\n", num1 + num2);
    printf("Subtraction: %d\n", num1 - num2);
    printf("Multiplication: %d\n", num1 * num2);
    printf("Division: %d\n", num1 / num2);
    printf("Modules: %d\n", num1 % num2);

    return 0;
}